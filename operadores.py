"""
- Soma : +
-Subtração : -
-Multiplicação : *
-Divisão : /
-Divisão Inteira : //
-Resto : %
-Potência : **
"""


numero1=10
numero2=20

print(numero1 + numero2)  # Soma
print(numero1 - numero2)  # Subtração 
print(numero1 * numero2)  # Multiplicação
print(numero1 / numero2)  # Divisão
print(numero1 // numero2)  # Divisão Inteira


print(20 // 3) 
print(20 % 3 )
print(2 ** 3 )


